    <h4><a>Invalidity benefits</a></h4>
    <p class="mb-3">

        Invalidity benefit is one of the long-term benefits offered by the Fund paid to a member who lost the ability to work due
        to physical or mental disability after the state certified by the Board of Physicians.
        <br>
    <h5><a>Invalidity benefits include:</a></h5>
    <h4>Gratuity</h4>
    <p>Invalidity gratuity is a lump sum amount paid to a member who contributed for a period not less than 60 months
        and lost their earning capacity due to physical and/or mental disability.</p>

    <h5>Invalidity Pension</h5>
    <p>TThis is monthly payment to the member upon ceases employment on Medical Board's recommendations basis to the rest of his/her life.</p>
    <h4>Qualifying conditions</h4>
    <ul>
        <li>Beneficiary must be a registered member and contributor to the Fund.</li>
        <li>Member should have contributed not less than 60 months before developing the relevant disability.</li>
        <li>Member not necessarily reach 55 or 60 years</li>
    </ul>
    <h4>Procedures for applying pension benefits.</h4>
    <h5>Member to be able to get disability benefits is required to submit the following documents: -</h5>
    <ul>
        <li>Report of the Board of Physicians which confirm loss of member's ability to work.</li>
        <li>Employer's letter confirming his / her retirement.</li>
        <li> Two recent colored passport sized photographs of the member</li>
         <li>ZSSF membership card </li>
        <li>A personal letter requesting retirement benefits.</li>
        <li>Application form (SSF10) which is available and filled in front of ZSSF Benefit's Officer.</li>
    </ul>
    <h4>Additional features to ZSSF benefit.</h4>
    <ul>
        <li>ZSSF benefit is greater than any other Social Security Fund in Tanzania.</li>
        <li>Benefit provided by ZSSF is paid immediately after member completes the relevant procedures.</li>
        <li>Payment takes place between ZSSF and responsible member and not through employer, other institutions or anyone else.</li>
        <li>ZSSF benefit do not discriminate; is provided to all members, nationals and non nationals of Tanzania, as stipulated in the United Nations Declaration on Social Security.</li>
        <li>Payment of Benefits information is confidential between ZSSF and member, or beneficiary's payees.</li>
        <li>Payments are paid in early day of the month by 25 or below, even if it is a weekend or in religious and national holiday</li>
    </ul>
