        <li>Employer's letter confirming her pregnant</li>
        <li> Personal letter requesting Maternity Benefit.</li>
         <li> Two recent colored passport sized photographs of the member.</li>
         <li> ZSSF membership card</li>
         <li>Bank details: name of the bank, account name and account number of the applicant.</li>
         <li>Application form SSF13 which is available and filled in front of ZSSF Benefit's Officer..</li>
    </ul>
